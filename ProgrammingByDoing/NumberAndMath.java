class NumberAndMath{
    public static void main(String[] args) {
        System.out.println("I will Count my chickens");
        System.out.println("Hens "+(25 + 30 / 6));  // Adding the Numbers in the Form INT
        System.out.println("Hens "+(25f + 30f / 6f));  // Adding the Numbers in the Form float
        System.out.println("Roosters "+(100 - 25 * 3 % 4));
        System.out.println("Now i will Count the Eggs ");
        System.out.println(3 + 5 + 1 - 5 + 4 % 2 - 1 / 4 + 6  ); // Modulus
        System.out.println(" Is it True that 3 + 2 < 5 - 7? ");
        System.out.println(3+2<5-7);
        System.out.println("What is 3+2? "+(3+2));
        System.out.println("What is 5-7? "+(5-7));
        System.out.println("Oh , Thats Why it's False ");
        System.out.println("How about SomeMore");
        System.out.println("Is it Greater? "+(5>-2));
        System.out.println("Is it greater or equal? "+(5>=-2));
        System.out.println("Is it less or equal? "+(5<=-2));
        System.out.println((7 / 4));  //Dividing in Int
        System.out.println((7.0 / 4.0));  //Dividing in Float 
    }
}